
public class Operator_10 {

	public static void main(String[] args) {
		
		boolean b1 = 6==7;
		
		System.out.println("b1 = " + b1);
		
		b1 = 6!=7;
		System.out.println("b1 = " + b1);
		
		b1 = 6<7;
		System.out.println("b1 = " + b1);
		
		b1 = 6>7;
		System.out.println("b1 = " + b1);
		
		b1 = 6<=7;
		System.out.println("b1 = " + b1);
		
		b1 = 6>=7;
		System.out.println("b1 = " + b1);
	
		


	}

}
